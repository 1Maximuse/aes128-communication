uint32_t rcon[10] = { 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36 };

void transpose(uint8_t* block) {
    uint8_t tmp = 0;
    for (uint8_t i = 0; i < 4; i++) {
        for (uint8_t j = i+1; j < 4; j++) {
            tmp = block[(i << 2) + j];
            block[(i << 2) + j] = block[(j << 2) + i];
            block[(j << 2) + i] = tmp;
        }
    }
}

void addroundkey(uint8_t* block, uint32_t* roundkey) {
    for (uint8_t i = 0; i < 4; i++) {
        for (uint8_t j = 0; j < 4; j++) {
            block[(i << 2) + j] ^= (roundkey[j] >> ((3-i) << 3)) & 0xFF;
        }
    }
}

void inversesubbytes(uint8_t* block) {
    for (uint8_t i = 0; i < 16; i++) {
        block[i] = inversesbox[block[i]];
    }
}

void inverseshiftrows(uint8_t* block) {
    for (uint8_t i = 1; i < 4; i++) {
        uint32_t* row = (uint32_t*)block + (i << 2);
        *row = ( *row << (i << 3) ) + ( *row >> ((4-i) << 3) );
    }
}

uint8_t multiply(uint8_t b, uint8_t amount) {
    uint8_t p = 0;
	uint8_t counter;
	uint8_t overflow;
	for(counter = 0; counter < 8; counter++) {
		if((amount & 1) == 1) 
			p ^= b;
		overflow = b >> 7;
		b <<= 1;
		if(overflow) 
			b ^= 0x1B;		
		amount >>= 1;
	}
	return p;
}

void inversemixcolumns(uint8_t* block) {
    for (uint8_t i = 0; i < 4; i++) {
        uint8_t col[4] = { block[i], block[4+i], block[8+i], block[12+i] };
        block[i]    = multiply(col[0], 0x0E) ^ multiply(col[1], 0x0B) ^ multiply(col[2], 0x0D) ^ multiply(col[3], 0x09);
        block[4+i]  = multiply(col[0], 0x09) ^ multiply(col[1], 0x0E) ^ multiply(col[2], 0x0B) ^ multiply(col[3], 0x0D);
        block[8+i]  = multiply(col[0], 0x0D) ^ multiply(col[1], 0x09) ^ multiply(col[2], 0x0E) ^ multiply(col[3], 0x0B);
        block[12+i] = multiply(col[0], 0x0B) ^ multiply(col[1], 0x0D) ^ multiply(col[2], 0x09) ^ multiply(col[3], 0x0E);
    }
}


void keyexpansion(uint8_t* key, uint32_t* expanded_key) {
    for (uint8_t i = 0; i < 4; i++) {
        uint8_t four_i = i << 2;
        expanded_key[i] =
            (key[four_i] << 24)
            + (key[four_i + 1] << 16)
            + (key[four_i + 2] << 8)
            + key[four_i + 3];
    }

    for (uint8_t i = 4; i < 44; i++) {
        uint32_t xor = expanded_key[i-1];

        if (i % 4 == 0) {
            xor = (xor << 8) + (xor >> 24);

            uint8_t b0 = sbox[xor >> 24];
            uint8_t b1 = sbox[(xor >> 16) & 0xFF];
            uint8_t b2 = sbox[(xor >> 8) & 0xFF];
            uint8_t b3 = sbox[xor & 0xFF];

            xor = (b0 << 24) + (b1 << 16) + (b2 << 8) + b3;
            xor ^= rcon[(i >> 2)-1] << 24;
        }

        expanded_key[i] = expanded_key[i-4] ^ xor;
    }
}

void aesdecrypt(uint8_t* block) {

    uint8_t key[16] = KEY;
    uint32_t expanded_key[44];
    memset(expanded_key, 0, 44*sizeof(uint32_t));

    keyexpansion(key, expanded_key);

    transpose(block);

    addroundkey(block, expanded_key + 40);

    for (uint8_t round = 1; round <= 10; round++) {
        inverseshiftrows(block);
        inversesubbytes(block);
        addroundkey(block, expanded_key + ((10-round) << 2));
        if (round != 10) inversemixcolumns(block);
    }
    
    transpose(block);

}


int recvdata(int conn_fd, char* filename) {
    FILE* f = fopen(filename, "wb");
    if (f == NULL) {
        printf("Cannot open file.\n");
        exit(1);
    }
    uint8_t buffer[16];
    memset(buffer, 0, 16);
    ssize_t len;
    while ((len = recv(conn_fd, buffer, 16, 0)) > 0) {
        aesdecrypt(buffer);
        fwrite(buffer, sizeof(uint8_t), len, f);
        memset(buffer, 0, 16);
    }
    fclose(f);
}

int main(int argc, char** argv) {
    if (argc != 2) {
        printf("No filename specified.\n");
        exit(1);
    }

    int sock_fd = socket(AF_INET, SOCK_STREAM, 0);
    if (sock_fd == -1) {
        printf("Cannot create socket.\n");
        exit(1);
    }

    struct sockaddr_in srv_address;

    bzero(&srv_address, sizeof(struct sockaddr_in));
    srv_address.sin_family = AF_INET;
    srv_address.sin_addr.s_addr = inet_addr("127.0.0.1");
    srv_address.sin_port = htons(10000);

    if (connect(sock_fd, (struct sockaddr*)&srv_address, sizeof(srv_address)) != 0) {
        printf("Cannot connect.\n");
        exit(1);
    }

    recvdata(sock_fd, argv[1]);

    close(sock_fd);
}